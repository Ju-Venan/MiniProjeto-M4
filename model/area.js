class Areas{
    constructor(id, nome, continente, pais, tipo_ecossistema, problemas, nivel_risco){
        this.id= id
        this.nome= nome
        this.continente = continente
        this.pais= pais
        this.tipo_ecossistema= tipo_ecossistema
        this.problemas= problemas
        this.nivel_risco = nivel_risco
    }
}

module.exports = Areas